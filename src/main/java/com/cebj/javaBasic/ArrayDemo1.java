package com.cebj.javaBasic;

public class ArrayDemo1 {
    public static void main(String[] args) {
        int[] arr = new int[3];
        for (int a : arr) {
            System.out.println(a);
        }
        String[] strs = new String[3];
        for (String a : strs) {
            System.out.println(a);
        }
    }
}
